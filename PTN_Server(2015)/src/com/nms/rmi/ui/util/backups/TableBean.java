package com.nms.rmi.ui.util.backups;

/**
 * 创建table的bean
 * 
 * @author kk
 * 
 */
public class TableBean {

	private String attributeName;	//属性名称 列名
	private String attributeType;	//类型
	private String isNull;			//是否可为null  如果否 值为  NOT NULL   如是是  值为""
	private String defaultValue;	//默认值
	private String extra;			//是否为自增
	private boolean isPri;			//是否为主键
	
	public String getAttributeName() {
		return attributeName;
	}
	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}
	public String getAttributeType() {
		return attributeType;
	}
	public void setAttributeType(String attributeType) {
		this.attributeType = attributeType;
	}
	public String getIsNull() {
		return isNull;
	}
	public void setIsNull(String isNull) {
		this.isNull = isNull;
	}
	public String getDefaultValue() {
		return defaultValue;
	}
	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}
	public String getExtra() {
		return extra;
	}
	public void setExtra(String extra) {
		this.extra = extra;
	}
	public boolean isPri() {
		return isPri;
	}
	public void setPri(boolean isPri) {
		this.isPri = isPri;
	}
	
}
