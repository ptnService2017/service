package com.nms.corba.ninterface.impl.performance;

import java.util.List;

/**
 * Corba历史性能过滤条件对象
 * @author sy
 *
 */
public class CorbaPerformance {
	
	private int siteId;
	private int slotId;
	private String portName;
	private List<Integer> layerList;//粒度： 15min,24h
	private long startTime;
	private long endTime;
	
	
	
	public int getSlotId() {
		return slotId;
	}
	public void setSlotId(int slotId) {
		this.slotId = slotId;
	}
	public int getSiteId() {
		return siteId;
	}
	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}
	public String getPortName() {
		return portName;
	}
	public void setPortName(String portName) {
		this.portName = portName;
	}
	public List<Integer> getLayerList() {
		return layerList;
	}
	public void setLayerList(List<Integer> layerList) {
		this.layerList = layerList;
	}
	public long getStartTime() {
		return startTime;
	}
	public void setStartTime(long startTime) {
		this.startTime = startTime;
	}
	public long getEndTime() {
		return endTime;
	}
	public void setEndTime(long endTime) {
		this.endTime = endTime;
	}
	
	
}
