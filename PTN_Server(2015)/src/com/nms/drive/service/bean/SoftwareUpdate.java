package com.nms.drive.service.bean;

public class SoftwareUpdate {
	
	private NEObject neObject = new NEObject();
	private int excursion;//偏移量
	private int dataLong;//下载包数据长度
	private int fileLong;//文件总长度
	private byte[] databag;//数据包
	
	
	public NEObject getNeObject() {
		return neObject;
	}
	public void setNeObject(NEObject neObject) {
		this.neObject = neObject;
	}
	public int getExcursion() {
		return excursion;
	}
	public void setExcursion(int excursion) {
		this.excursion = excursion;
	}
	public int getDataLong() {
		return dataLong;
	}
	public void setDataLong(int dataLong) {
		this.dataLong = dataLong;
	}
	public int getFileLong() {
		return fileLong;
	}
	public void setFileLong(int fileLong) {
		this.fileLong = fileLong;
	}
	public byte[] getDatabag() {
		return databag;
	}
	public void setDatabag(byte[] databag) {
		this.databag = databag;
	}
	
	
}
