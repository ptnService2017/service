package com.nms.drive.service.bean;

public class NniObject {

	private int portEnable = 0;// 端口使能 0/(1)=关/(开)
	private int workPattern = 0; // 工作模式
	private int Buffer802_3Enable = 0;// 802.3流控
	private int mtu = 1518; // MTU
	private int reserve = 0;// 备用,00// 备用
	private int qinqEnable = 0;// QinQ使能 0/1=关/开

	public int getPortEnable() {
		return portEnable;
	}

	public void setPortEnable(int portEnable) {
		this.portEnable = portEnable;
	}

	public int getWorkPattern() {
		return workPattern;
	}

	public void setWorkPattern(int workPattern) {
		this.workPattern = workPattern;
	}

	public int getBuffer802_3Enable() {
		return Buffer802_3Enable;
	}

	public void setBuffer802_3Enable(int buffer802_3Enable) {
		Buffer802_3Enable = buffer802_3Enable;
	}

	public int getMtu() {
		return mtu;
	}

	public void setMtu(int mtu) {
		this.mtu = mtu;
	}

	public int getReserve() {
		return reserve;
	}

	public void setReserve(int reserve) {
		this.reserve = reserve;
	}

	public int getQinqEnable() {
		return qinqEnable;
	}

	public void setQinqEnable(int qinqEnable) {
		this.qinqEnable = qinqEnable;
	}
}
