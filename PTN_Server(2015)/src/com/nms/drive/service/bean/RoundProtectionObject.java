package com.nms.drive.service.bean;
/**
 * 环网保护(04H)
 * @author 肖成
 *
 */
public class RoundProtectionObject {
	private int roundID = 0;      //环网ID
	private int returnType = 0;   //返回类型
	private int delayTime = 0;   //拖延时间
	private int logicRoundID =0;//逻辑环ID
	private int nodeID = 0;      //本站节点ID
	private int nodeCount = 0;   //环网节点总数
	private int eastPort = 0;    //东向端口
	private int eastSlot = 0;    //东向槽位
	private int westProt = 0;    //西向端口
	private int westSlot = 0;    //西向槽位
	private int protectType = 0;  //保护类型
	private int targetNodeId = 0;//目的节点id
	private int eastLspId = 0;//东向lsp id
	private int westLspId = 0;//西向lsp id
	
	private int waitTime = 0;//等待恢复时间
	
	public int getWaitTime() {
		return waitTime;
	}
	public void setWaitTime(int waitTime) {
		this.waitTime = waitTime;
	}
	public int getTargetNodeId() {
		return targetNodeId;
	}
	public void setTargetNodeId(int targetNodeId) {
		this.targetNodeId = targetNodeId;
	}
	public int getEastLspId() {
		return eastLspId;
	}
	public void setEastLspId(int eastLspId) {
		this.eastLspId = eastLspId;
	}
	public int getWestLspId() {
		return westLspId;
	}
	public void setWestLspId(int westLspId) {
		this.westLspId = westLspId;
	}
	//get   set  方法
	public int getRoundID() {
		return roundID;
	}
	public void setRoundID(int roundID) {
		this.roundID = roundID;
	}
	public int getReturnType() {
		return returnType;
	}
	public void setReturnType(int returnType) {
		this.returnType = returnType;
	}
	public int getDelayTime() {
		return delayTime;
	}
	public void setDelayTime(int delayTime) {
		this.delayTime = delayTime;
	}
	public int getLogicRoundID() {
		return logicRoundID;
	}
	public void setLogicRoundID(int logicRoundID) {
		this.logicRoundID = logicRoundID;
	}
	public int getNodeID() {
		return nodeID;
	}
	public void setNodeID(int nodeID) {
		this.nodeID = nodeID;
	}
	public int getNodeCount() {
		return nodeCount;
	}
	public void setNodeCount(int nodeCount) {
		this.nodeCount = nodeCount;
	}
	public int getEastPort() {
		return eastPort;
	}
	public void setEastPort(int eastPort) {
		this.eastPort = eastPort;
	}
	public int getEastSlot() {
		return eastSlot;
	}
	public void setEastSlot(int eastSlot) {
		this.eastSlot = eastSlot;
	}
	public int getWestProt() {
		return westProt;
	}
	public void setWestProt(int westProt) {
		this.westProt = westProt;
	}
	public int getWestSlot() {
		return westSlot;
	}
	public void setWestSlot(int westSlot) {
		this.westSlot = westSlot;
	}
	public int getProtectType() {
		return protectType;
	}
	public void setProtectType(int protectType) {
		this.protectType = protectType;
	}
	
	@Override
	public String toString(){
		return "东向端口："+eastPort +"    东向槽口"+eastSlot;
	}
}
