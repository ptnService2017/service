package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

/**
 * function:端口mac地址学习数目查询 和 基于vlan的mac地址学习数目查询
 * Bean d对象
 * @author zk
 *
 */
public class SecondMacStudyObject {
	
	private int vlan = 1;//vlanid
	private int num =1;//条目id
	private int portId =0;//端口号
	private List<String> macAddressList = new ArrayList<String>();
	private int macNum = 0;//mac条目数
	public int getVlan() {
		return vlan;
	}
	public void setVlan(int vlan) {
		this.vlan = vlan;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getPortId() {
		return portId;
	}
	public void setPortId(int portId) {
		this.portId = portId;
	}
	
	public int getMacNum() {
		return macNum;
	}
	public void setMacNum(int macNum) {
		this.macNum = macNum;
	}
	public List<String> getMacAddressList() {
		return macAddressList;
	}
	public void setMacAddressList(List<String> macAddressList) {
		this.macAddressList = macAddressList;
	}
	
}
