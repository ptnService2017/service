package com.nms.drive.service.bean;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * 
 * AlarmLineObject 线路告警
 * @author 彭冲
 *
 */
public class AlarmLineObject implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 2455541140860175720L;
	private int line = 0;// 线路
	private int lineInfoCount = 0;// 线路长度

	private List<AlarmInfoObject> alarmInfoObjectList = new ArrayList<AlarmInfoObject>();

	public int getLine() {
		return line;
	}

	public void setLine(int line) {
		this.line = line;
	}

	public int getLineInfoCount() {
		return lineInfoCount;
	}

	public void setLineInfoCount(int lineInfoCount) {
		this.lineInfoCount = lineInfoCount;
	}

	public List<AlarmInfoObject> getAlarmInfoObjectList() {
		return alarmInfoObjectList;
	}

	public void setAlarmInfoObjectList(List<AlarmInfoObject> alarmInfoObjectList) {
		this.alarmInfoObjectList = alarmInfoObjectList;
	}

}
