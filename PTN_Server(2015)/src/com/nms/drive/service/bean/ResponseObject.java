  package com.nms.drive.service.bean;

public class ResponseObject {
	private int direction = 0;//方向
	private int objScope = 0;//对象范围
	private String objTagNo = "";//对象标识号1, 对象标识号2, 对象标识号3, 对象标识号4
	private int streamNo = 0;//流水号
	private String state = "";//状态1, 状态2
	private String length = "";//长度1, 长度2, 长度3, 长度4
	private int configState = 0;//配置完成状态
	public int getDirection() {
		return direction;
	}
	public void setDirection(int direction) {
		this.direction = direction;
	}
	public int getObjScope() {
		return objScope;
	}
	public void setObjScope(int objScope) {
		this.objScope = objScope;
	}
	public String getObjTagNo() {
		return objTagNo;
	}
	public void setObjTagNo(String objTagNo) {
		this.objTagNo = objTagNo;
	}
	public int getStreamNo() {
		return streamNo;
	}
	public void setStreamNo(int streamNo) {
		this.streamNo = streamNo;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getLength() {
		return length;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public int getConfigState() {
		return configState;
	}
	public void setConfigState(int configState) {
		this.configState = configState;
	}
	
}
