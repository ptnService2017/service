package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class PerformanceMasterCardObject {

	private int masterCard = 0;// 盘类型
	private int masterCardAddress = 0;// 盘地址
	private int reserve = 0;// 备用
	private int masterStatue;//盘状态
	private int dataBlockCount = 0;// 性能数据块长度
	private List<PerformanceDataBlockObject> performanceDataBlockObjectList = new ArrayList<PerformanceDataBlockObject>();

	public List<PerformanceDataBlockObject> getPerformanceDataBlockObjectList() {
		return performanceDataBlockObjectList;
	}

	public void setPerformanceDataBlockObjectList(List<PerformanceDataBlockObject> performanceDataBlockObjectList) {
		this.performanceDataBlockObjectList = performanceDataBlockObjectList;
	}

	
	public int getMasterStatue() {
		return masterStatue;
	}

	public void setMasterStatue(int masterStatue) {
		this.masterStatue = masterStatue;
	}

	public int getMasterCard() {
		return masterCard;
	}

	public void setMasterCard(int masterCard) {
		this.masterCard = masterCard;
	}

	public int getDataBlockCount() {
		return dataBlockCount;
	}

	public void setDataBlockCount(int dataBlockCount) {
		this.dataBlockCount = dataBlockCount;
	}

	public int getMasterCardAddress() {
		return masterCardAddress;
	}

	public void setMasterCardAddress(int masterCardAddress) {
		this.masterCardAddress = masterCardAddress;
	}

	public int getReserve() {
		return reserve;
	}

	public void setReserve(int reserve) {
		this.reserve = reserve;
	}

}
