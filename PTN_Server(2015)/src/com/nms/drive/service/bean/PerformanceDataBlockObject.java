package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class PerformanceDataBlockObject {

	private String dateTime = "";// 起始 年-月-日 时:分:秒

	private int performanceLineCount = 0;// 性能线路数
	private String endTime = "";// 结束 年-月-日 时:分:秒
	
	private List<PerformanceLineObject> performanceLineObjectList = new ArrayList<PerformanceLineObject>();

	public List<PerformanceLineObject> getPerformanceLineObjectList() {
		return performanceLineObjectList;
	}

	public void setPerformanceLineObjectList(List<PerformanceLineObject> performanceLineObjectList) {
		this.performanceLineObjectList = performanceLineObjectList;
	}

	
	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public int getPerformanceLineCount() {
		return performanceLineCount;
	}

	public void setPerformanceLineCount(int performanceLineCount) {
		this.performanceLineCount = performanceLineCount;
	}

	public String getDateTime() {
		return dateTime;
	}

	public void setDateTime(String dateTime) {
		this.dateTime = dateTime;
	}
}
