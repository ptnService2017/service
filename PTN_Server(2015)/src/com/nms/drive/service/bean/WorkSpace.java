package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class WorkSpace {
	private int workSpaceNo = 0;//工作站号
	private String workSpaceIp = "";//工作站IP地址
	private List<WorkSpaceUser> workSpaceUsers = new ArrayList<WorkSpaceUser>();//工作站的用户数
	public int getWorkSpaceNo() {
		return workSpaceNo;
	}
	public void setWorkSpaceNo(int workSpaceNo) {
		this.workSpaceNo = workSpaceNo;
	}
	public String getWorkSpaceIp() {
		return workSpaceIp;
	}
	public void setWorkSpaceIp(String workSpaceIp) {
		this.workSpaceIp = workSpaceIp;
	}
	public List<WorkSpaceUser> getWorkSpaceUsers() {
		return workSpaceUsers;
	}
	public void setWorkSpaceUsers(List<WorkSpaceUser> workSpaceUsers) {
		this.workSpaceUsers = workSpaceUsers;
	}
	
}
