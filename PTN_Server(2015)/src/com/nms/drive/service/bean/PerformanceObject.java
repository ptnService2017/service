package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class PerformanceObject {

	private int performanceDataType = 0;// 性能数据类型
	private int neAddress = 0;// NE地址
	private int neStatus = 0;// NE状态
	private int reserve = 0;// 备用
	private int masterCardCount = 0;// 盘总数
	private List<PerformanceMasterCardObject> performanceMasterCardObjectList = new ArrayList<PerformanceMasterCardObject>();

	public int getMasterCardCount() {
		return masterCardCount;
	}

	public void setMasterCardCount(int masterCardCount) {
		this.masterCardCount = masterCardCount;
	}

	public List<PerformanceMasterCardObject> getPerformanceMasterCardObjectList() {
		return performanceMasterCardObjectList;
	}

	public void setPerformanceMasterCardObjectList(List<PerformanceMasterCardObject> performanceMasterCardObjectList) {
		this.performanceMasterCardObjectList = performanceMasterCardObjectList;
	}

	public int getPerformanceDataType() {
		return performanceDataType;
	}

	public void setPerformanceDataType(int performanceDataType) {
		this.performanceDataType = performanceDataType;
	}

	public int getNeAddress() {
		return neAddress;
	}

	public void setNeAddress(int neAddress) {
		this.neAddress = neAddress;
	}

	public int getNeStatus() {
		return neStatus;
	}

	public void setNeStatus(int neStatus) {
		this.neStatus = neStatus;
	}

	public int getReserve() {
		return reserve;
	}

	public void setReserve(int reserve) {
		this.reserve = reserve;
	}

}
