package com.nms.drive.service.bean.status;

public class LagPortObject {
	
	private int portNum;   //端口号
	private int portLacp;  //端口LACP协议
	private int portStatus;   //端口状态
	private int localPri;  //本地端口优先级
	public int getPortNum() {
		return portNum;
	}
	public void setPortNum(int portNum) {
		this.portNum = portNum;
	}
	public int getPortLacp() {
		return portLacp;
	}
	public void setPortLacp(int portLacp) {
		this.portLacp = portLacp;
	}
	public int getPortStatus() {
		return portStatus;
	}
	public void setPortStatus(int portStatus) {
		this.portStatus = portStatus;
	}
	public int getLocalPri() {
		return localPri;
	}
	public void setLocalPri(int localPri) {
		this.localPri = localPri;
	}
}
