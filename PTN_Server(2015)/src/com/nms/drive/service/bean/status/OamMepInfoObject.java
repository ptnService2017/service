package com.nms.drive.service.bean.status;

/**
 * 
 * 底层接入链路以太网oam对端MEP信息的驱动对象
 * @author zk
 *
 */
public class OamMepInfoObject {
	
	
	private String mepId;//对端MEP ID
	private String mepMac;//对端MEP MAC
	private String mdName;//MD name
	private String mdLevel;//MD Level
	private String maName;//MA name
	public String getMepId() {
		return mepId;
	}
	public void setMepId(String mepId) {
		this.mepId = mepId;
	}
	public String getMepMac() {
		return mepMac;
	}
	public void setMepMac(String mepMac) {
		this.mepMac = mepMac;
	}
	public String getMdName() {
		return mdName;
	}
	public void setMdName(String mdName) {
		this.mdName = mdName;
	}
	public String getMdLevel() {
		return mdLevel;
	}
	public void setMdLevel(String mdLevel) {
		this.mdLevel = mdLevel;
	}
	public String getMaName() {
		return maName;
	}
	public void setMaName(String maName) {
		this.maName = maName;
	}
	
	
}
