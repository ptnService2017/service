package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class CccObject {

	private int vplsId = 0;// VS ID
	private int enable = 1;// 使能 0/(1)=关/(开)
	private int macCount = 0;// MAC地址表容量(4)
	private int cccUNIObjCount = 1;// (LAN口成员列表)成员端口数

	private List<CccUNIObject> cccUNIObjectList = new ArrayList<CccUNIObject>();// (LAN口成员列表)成员端口数

	
	public List<CccUNIObject> getCccUNIObjectList() {
		return cccUNIObjectList;
	}

	public void setCccUNIObjectList(List<CccUNIObject> ccUNIObjectList) {
		cccUNIObjectList = ccUNIObjectList;
	}

	
	public int getVplsId() {
		return vplsId;
	}

	public void setVplsId(int vplsId) {
		this.vplsId = vplsId;
	}

	public int getEnable() {
		return enable;
	}

	public void setEnable(int enable) {
		this.enable = enable;
	}

	public int getMacCount() {
		return macCount;
	}

	public void setMacCount(int macCount) {
		this.macCount = macCount;
	}

	public int getCccUNIObjCount() {
		return cccUNIObjCount;
	}

	public void setCccUNIObjCount(int ccUNIObjCount) {
		cccUNIObjCount = ccUNIObjCount;
	}
	
}
