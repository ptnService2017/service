package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class ManagementObject {
	private int neSwitch = 0;//网元开关(高),网元开关(低)
	private int manageDataTag = 0;//管理数据标识=01
	private String year = "";//(配置日期时间)年 
	private String month = "";//月 
	private String day = "";//日 
	private String hour = "";//时 
	private String minute = "";//分 
	private String second = "";//秒
	private List<WorkSpace> workSpaces = new ArrayList<WorkSpace>();//工作站数
	private List<NE> nes = new ArrayList<NE>();//总网元数
	private String expendFlag = "";//扩展标志1, 扩展标志2, 扩展标志3, 扩展标志4

	 
	public int getNeSwitch() {
		return neSwitch;
	}
	public void setNeSwitch(int neSwitch) {
		this.neSwitch = neSwitch;
	}
	public int getManageDataTag() {
		return manageDataTag;
	}
	public void setManageDataTag(int manageDataTag) {
		this.manageDataTag = manageDataTag;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	public String getMinute() {
		return minute;
	}
	public void setMinute(String minute) {
		this.minute = minute;
	}
	public String getSecond() {
		return second;
	}
	public void setSecond(String second) {
		this.second = second;
	}
	public List<WorkSpace> getWorkSpaces() {
		return workSpaces;
	}
	public void setWorkSpaces(List<WorkSpace> workSpaces) {
		this.workSpaces = workSpaces;
	}
	public List<NE> getNes() {
		return nes;
	}
	public void setNes(List<NE> nes) {
		this.nes = nes;
	}
	public String getExpendFlag() {
		return expendFlag;
	}
	public void setExpendFlag(String expendFlag) {
		this.expendFlag = expendFlag;
	}
	
}
