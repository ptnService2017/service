package com.nms.drive.service.bean;

import java.util.ArrayList;
import java.util.List;

public class PerformanceLineObject {

	private int line = 0;// 线路号
	private int lineCount = 0;// 线路长度

	private List<PerformanceInfoObject> performanceInfoObjectList = new ArrayList<PerformanceInfoObject>();

	public int getLine() {
		return line;
	}

	public void setLine(int line) {
		this.line = line;
	}

	public int getLineCount() {
		return lineCount;
	}

	public void setLineCount(int lineCount) {
		this.lineCount = lineCount;
	}

	public List<PerformanceInfoObject> getPerformanceInfoObjectList() {
		return performanceInfoObjectList;
	}

	public void setPerformanceInfoObjectList(List<PerformanceInfoObject> performanceInfoObjectList) {
		this.performanceInfoObjectList = performanceInfoObjectList;
	}
}
