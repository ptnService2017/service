	private int portMode = 0;//端口模式
	private int relevanceVlan = 0;// VLAN关联设置 (0)/1=(不关联)/关联
	private int relevance802_1P = 0;// 802.1P关联设置 (0)/1=(不关联)/关联
	private int relevanceSourceMac = 0;// 源MAC地址关联设置 (0)/1=(不关联)/关联
	private int relevanceTargetMac = 0;// 目的MAC地址关联设置 (0)/1=(不关联)/关联
