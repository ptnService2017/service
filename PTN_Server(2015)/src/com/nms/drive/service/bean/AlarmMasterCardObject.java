package com.nms.drive.service.bean;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * 
 * AlarmMasterCardObject 某盘告警
 * @author 彭冲
 *
 */
public class AlarmMasterCardObject implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 4458577645378771102L;
	private int masterCard = 0;// 盘类型
	private int masterCardAddress = 0;// 盘地址
	private int masterCardFeature = 0;// 盘特征字
	private int reserve = 0;// 备用
	private int alarmLineCount = 0;// 告警线路数
	private List<AlarmLineObject> alarmLineObjectList = new ArrayList<AlarmLineObject>();

	public int getAlarmLineCount() {
		return alarmLineCount;
	}

	public void setAlarmLineCount(int alarmLineCount) {
		this.alarmLineCount = alarmLineCount;
	}

	public int getMasterCard() {
		return masterCard;
	}

	public void setMasterCard(int masterCard) {
		this.masterCard = masterCard;
	}

	public int getMasterCardAddress() {
		return masterCardAddress;
	}

	public void setMasterCardAddress(int masterCardAddress) {
		this.masterCardAddress = masterCardAddress;
	}

	public int getMasterCardFeature() {
		return masterCardFeature;
	}

	public void setMasterCardFeature(int masterCardFeature) {
		this.masterCardFeature = masterCardFeature;
	}

	public int getReserve() {
		return reserve;
	}

	public void setReserve(int reserve) {
		this.reserve = reserve;
	}

	public List<AlarmLineObject> getAlarmLineObjectList() {
		return alarmLineObjectList;
	}

	public void setAlarmLineObjectList(List<AlarmLineObject> alarmLineObjectList) {
		this.alarmLineObjectList = alarmLineObjectList;
	}

}
