package com.nms.drive.service.bean;
/**
 * 端口聚合对象 中的 出口队列调度策略 List
 * @author 罗磊
 *
 */
public class PortLAGexitQueue {
	private int mode = 0;// (出口队列调度策略)(队列0)模式 (0)/1=(SP)/WFQ 
	private int popedom = 1;// (队列0)权重 
	private int discardProbability = 50;
	
	public int getMode() {
		return mode;
	}
	public void setMode(int mode) {
		this.mode = mode;
	}
	public int getPopedom() {
		return popedom;
	}
	public void setPopedom(int popedom) {
		this.popedom = popedom;
	}
	
	public int getDiscardProbability() {
		return discardProbability;
	}
	
	public void setDiscardProbability(int discardProbability) {
		this.discardProbability = discardProbability;
	}

}
