package com.nms.drive.service.impl.bean;

import java.util.Date;

public class ResponseCommandObject {

	private Date responseDate = null;
	private int uuid = 0;// 报文序列号
	private int pagesCount = 0;// 分片号
	private int pagesMaxCount = 0;// 总片数
	private int ctrl = 0;// 控制字
	private int spare = 0;// 备用
	private int length = 0;// 数据长度
	private byte[] command = null;

	public int getUuid() {
		return uuid;
	}

	public void setUuid(int uuid) {
		this.uuid = uuid;
	}

	public int getPagesCount() {
		return pagesCount;
	}

	public void setPagesCount(int pagesCount) {
		this.pagesCount = pagesCount;
	}

	public int getPagesMaxCount() {
		return pagesMaxCount;
	}

	public void setPagesMaxCount(int pagesMaxCount) {
		this.pagesMaxCount = pagesMaxCount;
	}

	public int getCtrl() {
		return ctrl;
	}

	public void setCtrl(int ctrl) {
		this.ctrl = ctrl;
	}

	public int getSpare() {
		return spare;
	}

	public void setSpare(int spare) {
		this.spare = spare;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}

	public byte[] getCommand() {
		return command;
	}

	public void setCommand(byte[] command) {
		this.command = command;
	}

	public Date getResponseDate() {
		return responseDate;
	}

	public void setResponseDate(Date responseDate) {
		this.responseDate = responseDate;
	}

}
