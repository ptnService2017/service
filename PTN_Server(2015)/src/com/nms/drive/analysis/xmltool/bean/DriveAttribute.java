package com.nms.drive.analysis.xmltool.bean;

import java.util.ArrayList;
import java.util.List;

public class DriveAttribute {
	private String offset = "";
	private String length = "";
	private String description = "";
	private String value = "";
	private String mapping = "";
	private byte[] commandByteArray = null;
	private List<ListRoot> listRootList = new ArrayList<ListRoot>();
	
	public String getMapping() {
		return mapping;
	}

	public void setMapping(String mapping) {
		this.mapping = mapping;
	}

	public List<ListRoot> getListRootList() {
		return listRootList;
	}

	public void setListRootList(List<ListRoot> listRootList) {
		this.listRootList = listRootList;
	}

	public String getLength() {
		return length;
	}

	public void setLength(String length) {
		this.length = length;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public byte[] getCommandByteArray() {
		return commandByteArray;
	}

	public void setCommandByteArray(byte[] commandByteArray) {
		this.commandByteArray = commandByteArray;
	}

	public String getOffset() {
		return offset;
	}

	public void setOffset(String offset) {
		this.offset = offset;
	}

}
