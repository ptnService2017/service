package com.nms.db.bean.report;

import com.nms.ui.frame.ViewDataObj;

public class SSAlarm extends ViewDataObj{

	/**
	 * 
	 */
	private static final long serialVersionUID = 8902351037598492675L;

	@Override
	public void putObjectProperty() {
		this.putClientProperty("id",this.getID());
		this.putClientProperty("name",this.getName());
		this.putClientProperty("instancy",this.getInstancy());
		this.putClientProperty("mostly",this.getMostly());
		this.putClientProperty("subordination",this.getSubordination());
		this.putClientProperty("clew",this.getClew());
		this.putClientProperty("Unknow",this.getUnknow());
		this.putClientProperty("AlarmCount",this.getAlarmCount());
		
		
	}
	private String  id; //ID
	private String  name;  //告警源
	private int  instancy; //紧急告警
	private int  mostly; //主要告警
	private int  subordination; //次要告警
	private int  clew; //提示告警
	private int  Unknow ; //未确定告警
	private int  AlarmCount ; //总计
	private int siteid;

	public int getSiteid() {
		return siteid;
	}
	public void setSiteid(int siteid) {
		this.siteid = siteid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	@Override
	public String getName() {
		return name;
	}
	@Override
	public void setName(String name) {
		this.name = name;
	}
	public int getInstancy() {
		return instancy;
	}
	public void setInstancy(int instancy) {
		this.instancy = instancy;
	}
	public int getMostly() {
		return mostly;
	}
	public void setMostly(int mostly) {
		this.mostly = mostly;
	}
	public int getSubordination() {
		return subordination;
	}
	public void setSubordination(int subordination) {
		this.subordination = subordination;
	}
	public int getClew() {
		return clew;
	}
	public void setClew(int clew) {
		this.clew = clew;
	}
	public int getUnknow() {
		return Unknow;
	}
	public void setUnknow(int unknow) {
		Unknow = unknow;
	}
	public int getAlarmCount() {
		return AlarmCount;
	}
	public void setAlarmCount(int alarmCount) {
		AlarmCount = alarmCount;
	}
	
	

}
