package com.nms.db.bean.system;

import com.nms.ui.frame.ViewDataObj;

public class ELayerRateInfo extends ViewDataObj{
	private static final long serialVersionUID = -8805133824692527470L;
	private String objName;
	private int layerNumber;

	public String getObjName() {
		return objName;
	}

	public void setObjName(String objName) {
		this.objName = objName;
	}

	public int getLayerNumber() {
		return layerNumber;
	}

	public void setLayerNumber(int layerNumber) {
		this.layerNumber = layerNumber;
	}

	@Override
	public void putObjectProperty() {
		this.putClientProperty("objName", this.getObjName());
		this.putClientProperty("layerNumber", this.getLayerNumber());
	}

}
