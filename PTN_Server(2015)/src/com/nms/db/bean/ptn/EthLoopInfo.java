package com.nms.db.bean.ptn;

import java.io.Serializable;
/**
 * 以太网环回对象
 * @author zk
 */
public class EthLoopInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7750932153853278775L;
	
	private int id;
	private int siteId;
	private int loopEnable = 0;
	private int portNumber = 0;
	private int loopRule = 0;
	private String vlanVaue = "";
	private String macAddress = "";
	private String ip = "";
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getSiteId() {
		return siteId;
	}
	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}
	public int getLoopEnable() {
		return loopEnable;
	}
	public void setLoopEnable(int loopEnable) {
		this.loopEnable = loopEnable;
	}
	public int getPortNumber() {
		return portNumber;
	}
	public void setPortNumber(int portNumber) {
		this.portNumber = portNumber;
	}
	public int getLoopRule() {
		return loopRule;
	}
	public void setLoopRule(int loopRule) {
		this.loopRule = loopRule;
	}
	public String getMacAddress() {
		return macAddress;
	}
	public void setMacAddress(String macAddress) {
		this.macAddress = macAddress;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	public String getVlanVaue() {
		return vlanVaue;
	}
	public void setVlanVaue(String vlanVaue) {
		this.vlanVaue = vlanVaue;
	}
	
}
