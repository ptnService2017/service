package com.nms.db.bean.equipment.shelf;

import java.io.Serializable;
import java.util.List;

import com.nms.db.bean.equipment.slot.SlotInst;


@SuppressWarnings("serial")
public class EquipInst implements Serializable {
	private int id;
	private int siteId;
	private String imagePath;
	private int equipx;
	private int equipy;
	private List<SlotInst> slotlist;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getSiteId() {
		return siteId;
	}

	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}

	public String getImagePath() {
		return imagePath;
	}

	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}

	public int getEquipx() {
		return equipx;
	}

	public void setEquipx(int equipx) {
		this.equipx = equipx;
	}

	public int getEquipy() {
		return equipy;
	}

	public void setEquipy(int equipy) {
		this.equipy = equipy;
	}

	public List<SlotInst> getSlotlist() {
		return slotlist;
	}

	public void setSlotlist(List<SlotInst> slotlist) {
		this.slotlist = slotlist;
	}
}
