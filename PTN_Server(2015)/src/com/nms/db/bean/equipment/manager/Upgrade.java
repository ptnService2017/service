package com.nms.db.bean.equipment.manager;

import java.io.Serializable;
import java.util.List;

@SuppressWarnings("serial")
public class Upgrade implements Serializable {
	private int neAddress;// NE地址
	private int cardType = 160;
	private int cardNumber;// 盘组号
	private int cardAddress;// 盘地址
	private int fileLength;// 文件总长
	private int excursion;// 偏移量
	private int dataLong;// 下载包数据长度
	private byte[] dataEach;// 每个数据包的数据内容
	private List<byte[]> dataList;// 数据内容
	private int siteId;// 网元id



	public int getNeAddress() {
		return neAddress;
	}

	public void setNeAddress(int neAddress) {
		this.neAddress = neAddress;
	}

	public int getExcursion() {
		return excursion;
	}

	public void setExcursion(int excursion) {
		this.excursion = excursion;
	}

	public int getDataLong() {
		return dataLong;
	}

	public void setDataLong(int dataLong) {
		this.dataLong = dataLong;
	}

	public byte[] getDataEach() {
		return dataEach;
	}

	public void setDataEach(byte[] dataEach) {
		this.dataEach = dataEach;
	}

	public int getSiteId() {
		return siteId;
	}

	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}


	public List<byte[]> getDataList() {
		return dataList;
	}

	public void setDataList(List<byte[]> dataList) {
		this.dataList = dataList;
	}

	public int getCardType() {
		return cardType;
	}

	public void setCardType(int cardType) {
		this.cardType = cardType;
	}

	public int getCardNumber() {
		return cardNumber;
	}

	public void setCardNumber(int cardNumber) {
		this.cardNumber = cardNumber;
	}

	public int getCardAddress() {
		return cardAddress;
	}

	public void setCardAddress(int cardAddress) {
		this.cardAddress = cardAddress;
	}

	public int getFileLength() {
		return fileLength;
	}

	public void setFileLength(int fileLength) {
		this.fileLength = fileLength;
	}

}
