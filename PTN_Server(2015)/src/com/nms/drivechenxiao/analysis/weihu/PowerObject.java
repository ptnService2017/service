package com.nms.drivechenxiao.analysis.weihu;

import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * 光功率
 * @author sy
 *
 */
public class PowerObject {
	
	
	//--------端口下属性
	private String admin;//管理状态
	private String name;//端口名称
	private String sfpexptype;//SFP实际类型
	//----------当前性能中的    ：性能值
	private String lb;//激光器偏激电流
	private String lt;//激光温度
	private String iop;//光接收功率
	private String oop;//光发送功率
	
	/**
	 * Integer    在线板卡的   id
	 *  String    在线板卡上的 端口--对应板卡id 
	 */
	private Map<Integer,List<String>> portMap=new HashMap<Integer, List<String>>();
	

	public Map<Integer, List<String>> getPortMap() {
		return portMap;
	}
	public void setPortMap(Map<Integer, List<String>> portMap) {
		this.portMap = portMap;
	}
	public String getAdmin() {
		return admin;
	}
	public void setAdmin(String admin) {
		this.admin = admin;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSfpexptype() {
		return sfpexptype;
	}
	public void setSfpexptype(String sfpexptype) {
		this.sfpexptype = sfpexptype;
	}
	
	public String getLb() {
		return lb;
	}
	public void setLb(String lb) {
		this.lb = lb;
	}
	public String getLt() {
		return lt;
	}
	public void setLt(String lt) {
		this.lt = lt;
	}
	public String getIop() {
		return iop;
	}
	public void setIop(String iop) {
		this.iop = iop;
	}
	public String getOop() {
		return oop;
	}
	public void setOop(String oop) {
		this.oop = oop;
	}
	
}
