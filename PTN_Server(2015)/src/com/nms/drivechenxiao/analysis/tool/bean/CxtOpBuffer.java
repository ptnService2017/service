package com.nms.drivechenxiao.analysis.tool.bean;

import java.util.ArrayList;
import java.util.List;

public class CxtOpBuffer {

	private int opCount = 0;
	private List<CxtOpItem> cxtOpItems = new ArrayList<CxtOpItem>();

	public int getOpCount() {
		return opCount;
	}

	public void setOpCount(int opCount) {
		this.opCount = opCount;
	}

	public List<CxtOpItem> getCxtOpItems() {
		return cxtOpItems;
	}

	public void setCxtOpItems(List<CxtOpItem> cxtOpItems) {
		this.cxtOpItems = cxtOpItems;
	}
}
