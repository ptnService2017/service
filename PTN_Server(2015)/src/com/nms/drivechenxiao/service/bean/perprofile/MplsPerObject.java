package com.nms.drivechenxiao.service.bean.perprofile;
/**性能配置**/
public class MplsPerObject {
	private String name;
	private String type;//MPLS
	private String tpall;//发送包总数
	private String toall;//发送字节总数
	private String rpall;//接收包总数
	private String roall;//接收字节总数
	private String dropratio;//丢包率
	private String latency;//时延
	private String jitter;//时延变化
	private String ref;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTpall() {
		return tpall;
	}
	public void setTpall(String tpall) {
		this.tpall = tpall;
	}
	public String getToall() {
		return toall;
	}
	public void setToall(String toall) {
		this.toall = toall;
	}
	public String getRpall() {
		return rpall;
	}
	public void setRpall(String rpall) {
		this.rpall = rpall;
	}
	public String getRoall() {
		return roall;
	}
	public void setRoall(String roall) {
		this.roall = roall;
	}
	public String getDropratio() {
		return dropratio;
	}
	public void setDropratio(String dropratio) {
		this.dropratio = dropratio;
	}
	public String getLatency() {
		return latency;
	}
	public void setLatency(String latency) {
		this.latency = latency;
	}
	public String getJitter() {
		return jitter;
	}
	public void setJitter(String jitter) {
		this.jitter = jitter;
	}
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	
	
}
