package com.nms.drivechenxiao.service.bean.xc;

/**
 * 路由转发
 * @author sy
 *
 */
public class Route {
	String name;
	String nexthop;
	String ifname;
	String type;
	String metric;
	
	public String toString(){
		return new StringBuffer().append(" name=").append(name)
		.append(" ;nexthop=").append(nexthop).append(" ;ifname=").append(ifname)
		.append(" ;type=").append(type).append(" ;metric=").append(metric).toString();
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNexthop() {
		return nexthop;
	}
	public void setNexthop(String nexthop) {
		this.nexthop = nexthop;
	}
	public String getIfname() {
		return ifname;
	}
	public void setIfname(String ifname) {
		this.ifname = ifname;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getMetric() {
		return metric;
	}
	public void setMetric(String metric) {
		this.metric = metric;
	}
	
}
