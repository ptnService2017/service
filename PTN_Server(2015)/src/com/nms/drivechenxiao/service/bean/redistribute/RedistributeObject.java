package com.nms.drivechenxiao.service.bean.redistribute;

public class RedistributeObject {
	private String redistribute_type = "";
	private String type = "";
	private String metrictype = "";
	private int metric = 0;
	private int enable = 0;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMetrictype() {
		return metrictype;
	}

	public void setMetrictype(String metrictype) {
		this.metrictype = metrictype;
	}

	public String getRedistribute_type() {
		return redistribute_type;
	}

	public void setRedistribute_type(String redistribute_type) {
		this.redistribute_type = redistribute_type;
	}

	public int getMetric() {
		return metric;
	}

	public void setMetric(int metric) {
		this.metric = metric;
	}

	public int getEnable() {
		return enable;
	}

	public void setEnable(int enable) {
		this.enable = enable;
	}

}
