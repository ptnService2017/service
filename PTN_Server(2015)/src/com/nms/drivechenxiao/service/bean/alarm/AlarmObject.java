package com.nms.drivechenxiao.service.bean.alarm;

import com.nms.drivechenxiao.service.bean.CXNEObject;

public class AlarmObject {
	private CXNEObject cXNEObject = null;
	private String objId = "";
	private String almId = "";
	private String level = "";
	private String time = "";
	private String code = "";
	private String status = "";
	private String perid="";//[1,4] ; 1="15分钟性能低门限越限" ,2="15分钟性能高门限越限" ,3="24小时性能低门限越限" ,4="24小时性能高门限越限" 
	private String perVal="";//[1,110]对应.htm.的val

	public String toString(){
		return new StringBuffer().append("objId=").append(objId)
		.append(" ;almId=").append(almId).append(" ;level=").append(level)
		.append(" ;level=").append(level).append(" ;time=").append(time)
		.append(" ;code=").append(code).append(" ;status=").append(status)
		.append(" ;perid=").append(perid).append(" ;perVal=").append(perVal).toString();
	}
	public String getPerid() {
		return perid;
	}

	public void setPerid(String perid) {
		this.perid = perid;
	}

	public String getPerVal() {
		return perVal;
	}

	public void setPerVal(String perVal) {
		this.perVal = perVal;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public CXNEObject getCXNEObject() {
		return cXNEObject;
	}

	public void setCXNEObject(CXNEObject object) {
		cXNEObject = object;
	}

	public String getObjId() {
		return objId;
	}

	public void setObjId(String objId) {
		this.objId = objId;
	}

	public String getAlmId() {
		return almId;
	}

	public void setAlmId(String almId) {
		this.almId = almId;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

}
