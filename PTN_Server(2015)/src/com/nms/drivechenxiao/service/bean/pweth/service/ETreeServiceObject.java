package com.nms.drivechenxiao.service.bean.pweth.service;

public class ETreeServiceObject {
	private String type = "";
	private String vpn = "";
	private String hwres = "";
	private String learn = "";// 地址学习(true|false)
	private String role = "";// ETREE 接口角色(root|branch)
	
	public String toString(){
		StringBuffer sb=new StringBuffer().append(" type=").append(type)
		.append(" ;vpn=").append(vpn).append(" ;hwres=").append(hwres)
		.append(" ;learn=").append(learn).append(" ;role=").append(role);
		return sb.toString();
	}
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getVpn() {
		return vpn;
	}

	public void setVpn(String vpn) {
		this.vpn = vpn;
	}

	public String getHwres() {
		return hwres;
	}

	public void setHwres(String hwres) {
		this.hwres = hwres;
	}

	public String getLearn() {
		return learn;
	}

	public void setLearn(String learn) {
		this.learn = learn;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

}
