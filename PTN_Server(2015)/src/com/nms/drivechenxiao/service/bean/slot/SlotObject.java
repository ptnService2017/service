package com.nms.drivechenxiao.service.bean.slot;

import com.nms.drivechenxiao.service.bean.CXNEObject;

public class SlotObject {
	private CXNEObject cXNEObject = null;
	private String id = "";
	private String type = "";
	private String online="";//板卡在线状态  bool
	public CXNEObject getCXNEObject() {
		return cXNEObject;
	}

	public String getOnline() {
		return online;
	}

	public void setOnline(String online) {
		this.online = online;
	}

	public void setCXNEObject(CXNEObject object) {
		cXNEObject = object;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
