package com.nms.drivechenxiao.service.bean.clock;



//外时钟配置
//ne/interfaces/extclk/
public class ExtclkObject {
	private String name;
	private String ifname ; //接口名
	private String scspri ;//scs优先级 ;  int [0,255]
	private String ifindex ; //接口索引
	private String desc ; //接口描述 ; txt 1-16
	private String admin ; //接口管理状态  ; up down
	private String oper ; // 接口工作状 态 ; //CoderUtils.analysisOper(CoderUtils.intTo6Binary(Integer.valueOf(value)))
	private String ref ; //u32 0x0
	private String scsmanual ; //scs人工倒换 ; bool
	private String scsforce ; //scs强制倒换 ; bool
	private String scslockout ; //scs闭锁; bool
	private String forcelevel ; //强制等级 ; prc=1,ssua=2,ssub=3,sec=4,dnu=5,inv=6,auto=255
	private String ssmslot ; //ssm接收时隙 ;sa4=0,sa5=1,sa6=2,sa7=3,sa8=4
	private String ssmoutputenable ; //ssm输出使能 bool
	private String impedance ; //输入阻抗 ; ohm120=0, ohm75=1, ; 默认 = 1
	private String extclkmode ; //模式  ； bit2m=0 ,hz2m=1 
	private String PhysicalState ; //物理状态 ;  fail=0 ,normal=1 ,wtr=2等待恢复 ,na=3 无效
	private String SCSLogicalState ; //scs逻辑状态 ; fail=0 ,normal=1 ,wtr=2等待恢复 ,na=3 无效
	private String loopback ; //接口回环 ; normal=0 正常(解回环),internal=1(数据流内回环) ,external=2 
	private String SSMCode ; //ssm值 int ;[0,15]
	private String SCSQL ; //scs质量等级 prc=1,ssua=2,ssub=3,sec=4,dnu=5,inv=6,auto=255
	private String timeoffset ;//时间偏移补偿 ;int [0,1000000000]
	private String mode ; //tod接口类型：输入，输出 ; in=0,out=1
	
	
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getTimeoffset() {
		return timeoffset;
	}
	public void setTimeoffset(String timeoffset) {
		this.timeoffset = timeoffset;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIfname() {
		return ifname;
	}
	public void setIfname(String ifname) {
		this.ifname = ifname;
	}
	public String getScspri() {
		return scspri;
	}
	public void setScspri(String scspri) {
		this.scspri = scspri;
	}
	public String getIfindex() {
		return ifindex;
	}
	public void setIfindex(String ifindex) {
		this.ifindex = ifindex;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getAdmin() {
		return admin;
	}
	public void setAdmin(String admin) {
		this.admin = admin;
	}
	public String getOper() {
		return oper;
	}
	public void setOper(String oper) {
		this.oper = oper;
	}
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public String getScsmanual() {
		return scsmanual;
	}
	public void setScsmanual(String scsmanual) {
		this.scsmanual = scsmanual;
	}
	public String getScsforce() {
		return scsforce;
	}
	public void setScsforce(String scsforce) {
		this.scsforce = scsforce;
	}
	public String getScslockout() {
		return scslockout;
	}
	public void setScslockout(String scslockout) {
		this.scslockout = scslockout;
	}
	public String getForcelevel() {
		return forcelevel;
	}
	public void setForcelevel(String forcelevel) {
		this.forcelevel = forcelevel;
	}
	public String getSsmslot() {
		return ssmslot;
	}
	public void setSsmslot(String ssmslot) {
		this.ssmslot = ssmslot;
	}
	public String getSsmoutputenable() {
		return ssmoutputenable;
	}
	public void setSsmoutputenable(String ssmoutputenable) {
		this.ssmoutputenable = ssmoutputenable;
	}
	public String getImpedance() {
		return impedance;
	}
	public void setImpedance(String impedance) {
		this.impedance = impedance;
	}
	public String getExtclkmode() {
		return extclkmode;
	}
	public void setExtclkmode(String extclkmode) {
		this.extclkmode = extclkmode;
	}
	public String getPhysicalState() {
		return PhysicalState;
	}
	public void setPhysicalState(String physicalState) {
		PhysicalState = physicalState;
	}
	public String getSCSLogicalState() {
		return SCSLogicalState;
	}
	public void setSCSLogicalState(String logicalState) {
		SCSLogicalState = logicalState;
	}
	public String getLoopback() {
		return loopback;
	}
	public void setLoopback(String loopback) {
		this.loopback = loopback;
	}
	public String getSSMCode() {
		return SSMCode;
	}
	public void setSSMCode(String code) {
		SSMCode = code;
	}
	public String getSCSQL() {
		return SCSQL;
	}
	public void setSCSQL(String scsql) {
		SCSQL = scsql;
	}
	
	
}
