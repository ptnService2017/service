package com.nms.drivechenxiao.service.bean.clock;

//网元时钟状态/属性
public class ClockObject {
	private String ssmmode ;//ssm模式  ;bool
	private String scswtr ; //scs等待恢复时间 ; int [0,12]
	private String ecswtr ; //ecs等待恢复时间 ; int [0,12]
	private String freerunlevel ; //自由震荡等级 ; prc=1 ;ssua=2 ; ssub=3 ;sec=4 ;dnu=5 ;inv=6 ; auto=255 
	private String unknownlevel ; //ql_unk 等级 ; prc=1 ;ssua=2 ; ssub=3 ;sec=4 ;dnu=5 ;inv=6 ; auto=255
	private String operationmode ; //操作模式 auto=0 ; freerun=1 ; holdover = 2 ;
	private String extclkdrvmode ; //外定时导出模式 ; setg=0 ; ecs = 1 ;
	private String squelchmin ; //压制门限 ;prc=1 ;ssua=2 ; ssub=3 ;sec=4 ;dnu=5 ;inv=6 ; auto=255
	private String SCSWorkState ; //系统时钟锁相环状态 ; freerun=0; prelock=1 ; lock=2 ;holdover=3 ;outoflock=4 
	private String ECSWorkState ; //外时钟锁相环状态 ; freerun=0; prelock=1 ; lock=2 ;holdover=3 ;outoflock=4 
	private String SCSSelectSource ; //系统时钟选择器当前源选择 txt
	private String ECSSelectSource; //外时钟选择器当前源选择 txt
	private Ieee1588Object ieee1588 = new Ieee1588Object();
	
	
	public String getSsmmode() {
		return ssmmode;
	}
	public void setSsmmode(String ssmmode) {
		this.ssmmode = ssmmode;
	}
	public String getScswtr() {
		return scswtr;
	}
	public void setScswtr(String scswtr) {
		this.scswtr = scswtr;
	}
	public String getEcswtr() {
		return ecswtr;
	}
	public void setEcswtr(String ecswtr) {
		this.ecswtr = ecswtr;
	}
	public String getFreerunlevel() {
		return freerunlevel;
	}
	public void setFreerunlevel(String freerunlevel) {
		this.freerunlevel = freerunlevel;
	}
	public String getUnknownlevel() {
		return unknownlevel;
	}
	public void setUnknownlevel(String unknownlevel) {
		this.unknownlevel = unknownlevel;
	}
	public String getOperationmode() {
		return operationmode;
	}
	public void setOperationmode(String operationmode) {
		this.operationmode = operationmode;
	}
	public String getExtclkdrvmode() {
		return extclkdrvmode;
	}
	public void setExtclkdrvmode(String extclkdrvmode) {
		this.extclkdrvmode = extclkdrvmode;
	}
	public String getSquelchmin() {
		return squelchmin;
	}
	public void setSquelchmin(String squelchmin) {
		this.squelchmin = squelchmin;
	}
	public String getSCSWorkState() {
		return SCSWorkState;
	}
	public void setSCSWorkState(String workState) {
		SCSWorkState = workState;
	}
	public String getECSWorkState() {
		return ECSWorkState;
	}
	public void setECSWorkState(String workState) {
		ECSWorkState = workState;
	}
	public String getSCSSelectSource() {
		return SCSSelectSource;
	}
	public void setSCSSelectSource(String selectSource) {
		SCSSelectSource = selectSource;
	}
	public String getECSSelectSource() {
		return ECSSelectSource;
	}
	public void setECSSelectSource(String selectSource) {
		ECSSelectSource = selectSource;
	}
	public Ieee1588Object getIeee1588() {
		return ieee1588;
	}
	public void setIeee1588(Ieee1588Object ieee1588) {
		this.ieee1588 = ieee1588;
	}
	
	
}
