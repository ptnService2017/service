package com.nms.drivechenxiao.service.bean.user;

import com.nms.drivechenxiao.service.bean.CXNEObject;

public class UserObject {
	private CXNEObject cXNEObject = null;
	private String name = "";
	private String passWord = "";
	private String power = "";
	private String idle = "";
	private String newPassWord = "";
	private String newPassWordToo = "";
	private String lock = "";

	public String getPassWord() {
		return passWord;
	}

	public void setPassWord(String passWord) {
		this.passWord = passWord;
	}

	public String getNewPassWord() {
		return newPassWord;
	}

	public void setNewPassWord(String newPassWord) {
		this.newPassWord = newPassWord;
	}

	public String getNewPassWordToo() {
		return newPassWordToo;
	}

	public void setNewPassWordToo(String newPassWordToo) {
		this.newPassWordToo = newPassWordToo;
	}

	public String getLock() {
		return lock;
	}

	public void setLock(String lock) {
		this.lock = lock;
	}

	public CXNEObject getCXNEObject() {
		return cXNEObject;
	}

	public void setCXNEObject(CXNEObject object) {
		cXNEObject = object;
	}

	public String getName() {
		return name;
	}

	public void setName(String user) {
		this.name = user;
	}

	public String getPower() {
		return power;
	}

	public void setPower(String power) {
		this.power = power;
	}

	public String getIdle() {
		return idle;
	}

	public void setIdle(String idle) {
		this.idle = idle;
	}
}
