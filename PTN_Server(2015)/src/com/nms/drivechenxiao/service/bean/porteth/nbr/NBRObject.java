package com.nms.drivechenxiao.service.bean.porteth.nbr;

public class NBRObject {
	private String stat = "";
	private String smac = ""; // 用户配置的静态MAC地址
	private String mac = ""; // 邻居网元对应端口的MAC地址
	private String ifidx = ""; // 邻居网元对应端口的接口编号
	private String neid = ""; // 邻居网元ID
	private String ccn = ""; // 是否能够层载CCN
	
	public String toString(){
		StringBuffer sb=new StringBuffer().append(" stat=").append(stat)
		.append(" ;smac=").append(smac).append(" ;mac=").append(mac).append(" ;ifidx=").append(ifidx)
		.append(" ;neid=").append(neid).append(" ;ccn=").append(ccn);
		return sb.toString();
	}
	public String getStat() {
		return stat;
	}

	public void setStat(String stat) {
		this.stat = stat;
	}

	public String getSmac() {
		return smac;
	}

	public void setSmac(String smac) {
		this.smac = smac;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String getIfidx() {
		return ifidx;
	}

	public void setIfidx(String ifidx) {
		this.ifidx = ifidx;
	}

	public String getNeid() {
		return neid;
	}

	public void setNeid(String neid) {
		this.neid = neid;
	}

	public String getCcn() {
		return ccn;
	}

	public void setCcn(String ccn) {
		this.ccn = ccn;
	}

}
