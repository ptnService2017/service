package com.nms.drivechenxiao.service.bean.porteth.qos;

public class AFObject {
	private String cir = ""; // 承诺信息速率 **
	private String used = ""; // 当前使用带宽
	private String weight = ""; // 权重
	private String greenwredstart = ""; // 绿色业务WRED丢弃低门限,单位为KB
	private String greenwredend = ""; // 绿色业务WRED丢弃高门限,单位为KB
	private String greendroprate = ""; // 绿色业务WRED 丢弃概率
	private String yellowwredstart = ""; // 黄色业务WRED丢弃低门限,单位为KB
	private String yellowwredend = ""; // 黄色业务WRED丢弃高门限,单位为KB
	private String yellowdroprate = ""; // 黄色业务WRED 丢弃概率
	private String wreden = ""; // 使能WREAD
	private String maxbw = ""; // SP和SP+WDRR情况下最大带宽

	public String toString(){
		return new StringBuffer().append(" cir=").append(cir)
		.append(" ;used=").append(used).append(" ;weight=").append(weight)
		.append(" ;greenwredstart=").append(greenwredstart).append(" ;greenwredend=").append(greenwredend)
		.append(" ;greendroprate=").append(greendroprate).append(" ;yellowwredstart=").append(yellowwredstart)
		.append(" ;yellowwredend=").append(yellowwredend).append(" ;yellowdroprate=").append(yellowdroprate)
		.append(" ;wreden=").append(wreden).append(" ;maxbw=").append(maxbw).toString();
	}
	public String getCir() {
		return cir;
	}

	public void setCir(String cir) {
		this.cir = cir;
	}

	public String getUsed() {
		return used;
	}

	public void setUsed(String used) {
		this.used = used;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getGreenwredstart() {
		return greenwredstart;
	}

	public void setGreenwredstart(String greenwredstart) {
		this.greenwredstart = greenwredstart;
	}

	public String getGreenwredend() {
		return greenwredend;
	}

	public void setGreenwredend(String greenwredend) {
		this.greenwredend = greenwredend;
	}

	public String getGreendroprate() {
		return greendroprate;
	}

	public void setGreendroprate(String greendroprate) {
		this.greendroprate = greendroprate;
	}

	public String getYellowwredstart() {
		return yellowwredstart;
	}

	public void setYellowwredstart(String yellowwredstart) {
		this.yellowwredstart = yellowwredstart;
	}

	public String getYellowwredend() {
		return yellowwredend;
	}

	public void setYellowwredend(String yellowwredend) {
		this.yellowwredend = yellowwredend;
	}

	public String getYellowdroprate() {
		return yellowdroprate;
	}

	public void setYellowdroprate(String yellowdroprate) {
		this.yellowdroprate = yellowdroprate;
	}

	public String getWreden() {
		return wreden;
	}

	public void setWreden(String wreden) {
		this.wreden = wreden;
	}

	public String getMaxbw() {
		return maxbw;
	}

	public void setMaxbw(String maxbw) {
		this.maxbw = maxbw;
	}

}
