package com.nms.drivechenxiao.service.bean.protocols.mpls;

/**for 环网保护 add by stones on 20130916
 * **/
public class RingObject {
	private String name ; 
	private String desc = null; //describe
	private String enaps ; //enable aps    boolean
	private String wtrtime ; //recover time [60,720]  int
	private String holdofftime ; //delay time [0,200]  int
	private String nodeid ; //当前节点 id [1,127]  int
	private String westnbrid ; //西节点 id [1,127]  int
	private String eastnbrid ; //东 节点 id [1,127]  int
	private String westport ; //西节点 端口 
	private String eastport ; //东节点 端口
	private String apscmd ; //the manual switch command ;command(clear,lock.forceswitch,manualswitch);for example : forceswitch::east
	private String stat ; //Aps state 0=idle ; 1=passthrough ; 2=switch 
	private String westlastrxpdu ;//the west port latest received APS PDU. for example : 0x2020000
	private String eastlastrxpdu ;//the east port latest received APS PDU. for example : 0x1010000
	private String westlasttxpdu ; //the west port lastest sended APS PDU. for example : 0x1010000
	private String eastlasttxpdu ; //the east port lastest sended APS PDU. for example : 0x1010000
	
	public String toString(){
		return new StringBuffer().append(" name=").append(name)
		.append(" ;desc=").append(desc).append(" ;enaps=").append(enaps)
		.append(" ;wtrtime=").append(wtrtime).append(" ;holdofftime=").append(holdofftime)
		.append(" ;nodeid=").append(nodeid).append(" ;westnbrid=").append(westnbrid)
		.append(" ;eastnbrid=").append(eastnbrid).append(" ;westport=").append(westport)
		.append(" ;eastport=").append(eastport).append(" ;apscmd=").append(apscmd)
		.append(" ;stat=").append(stat).append(" ;westlastrxpdu=").append(westlastrxpdu)
		.append(" ;eastlastrxpdu=").append(eastlastrxpdu).append(" ;westlasttxpdu=").append(westlasttxpdu)
		.append(" ;eastlasttxpdu=").append(eastlasttxpdu).toString();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getEnaps() {
		return enaps;
	}
	public void setEnaps(String enaps) {
		this.enaps = enaps;
	}
	public String getWtrtime() {
		return wtrtime;
	}
	public void setWtrtime(String wtrtime) {
		this.wtrtime = wtrtime;
	}
	public String getHoldofftime() {
		return holdofftime;
	}
	public void setHoldofftime(String holdofftime) {
		this.holdofftime = holdofftime;
	}
	public String getNodeid() {
		return nodeid;
	}
	public void setNodeid(String nodeid) {
		this.nodeid = nodeid;
	}
	public String getWestnbrid() {
		return westnbrid;
	}
	public void setWestnbrid(String westnbrid) {
		this.westnbrid = westnbrid;
	}
	public String getEastnbrid() {
		return eastnbrid;
	}
	public void setEastnbrid(String eastnbrid) {
		this.eastnbrid = eastnbrid;
	}
	public String getWestport() {
		return westport;
	}
	public void setWestport(String westport) {
		this.westport = westport;
	}
	public String getEastport() {
		return eastport;
	}
	public void setEastport(String eastport) {
		this.eastport = eastport;
	}
	public String getApscmd() {
		return apscmd;
	}
	public void setApscmd(String apscmd) {
		this.apscmd = apscmd;
	}
	public String getStat() {
		return stat;
	}
	public void setStat(String stat) {
		this.stat = stat;
	}
	public String getWestlastrxpdu() {
		return westlastrxpdu;
	}
	public void setWestlastrxpdu(String westlastrxpdu) {
		this.westlastrxpdu = westlastrxpdu;
	}
	public String getEastlastrxpdu() {
		return eastlastrxpdu;
	}
	public void setEastlastrxpdu(String eastlastrxpdu) {
		this.eastlastrxpdu = eastlastrxpdu;
	}
	public String getWestlasttxpdu() {
		return westlasttxpdu;
	}
	public void setWestlasttxpdu(String westlasttxpdu) {
		this.westlasttxpdu = westlasttxpdu;
	}
	public String getEastlasttxpdu() {
		return eastlasttxpdu;
	}
	public void setEastlasttxpdu(String eastlasttxpdu) {
		this.eastlasttxpdu = eastlasttxpdu;
	}
	
}
