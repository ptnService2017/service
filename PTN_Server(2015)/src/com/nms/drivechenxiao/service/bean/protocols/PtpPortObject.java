package com.nms.drivechenxiao.service.bean.protocols;

import java.util.ArrayList;
import java.util.List;

public class PtpPortObject {
	private String portname ; //端口名
	private String portadmin ; //端口使能
	private boolean isPtp=false;
	private String porttype ; //端口类型(non_transparent) non_transparent=1非透明 ,transparent=2透明
	private String delaymechanism ; // 延时机制 (e2e) e2e=1端到端 , p2p=2 点到点
	private String anncintv ; //annc时间间隔 (1) int [-4,4]
	private String annctmo ; //annc接收超时(4) int [1,65535]
	private String syncintv ; //Sync时间间隔 (0) int [-8,1]
	private String delreqintv ; //DelayReq时间间隔(0) int [-4,4]
	private String pdelreqintv ; //pdelreq时间间隔(0) int [-4,4]
	private String delayoffset ; //延时补偿(0) int [-10000000,10000000]
	private String testdelayoffset ;//测出来的延时补偿(0); int  [-10000000,10000000]
	private String enable ; //端口使能 (false); bool
	private String vlanid ; //vlan号 (1) ; int [0,4094]
	private String operationmode ; //操作模式 (auto) ; auto=0自动选源 ,master=1主状态, slave=2从状态 ,passive=3 被动状态
	private String portstate ; //端口状态(init); init=1初始化 ,fault=2错误状态 ,disabled=3禁用, listening=4监听 ,pre_master=5预主状态 ,master=6主状态 ,passive=7被动状态 , uncalibrated=8无法确定状态,slave=9 从状态
	private String ref ; //u32
	private String two_step ; //tow_step模式(true) ; bool
	private List<String> portlist = new ArrayList<String>();
	
	
	public List<String> getPortlist() {
		return portlist;
	}
	public void setPortlist(List<String> portlist) {
		this.portlist = portlist;
	}
	public boolean getIsPtp() {
		return isPtp;
	}
	public void setIsptp(boolean isPtp) {
		this.isPtp = isPtp;
	}
	public String getPortname() {
		return portname;
	}
	public void setPortname(String portname) {
		this.portname = portname;
	}
	public String getPortadmin() {
		return portadmin;
	}
	public void setPortadmin(String portadmin) {
		this.portadmin = portadmin;
	}
	public String getPorttype() {
		return porttype;
	}
	public void setPorttype(String porttype) {
		this.porttype = porttype;
	}
	public String getDelaymechanism() {
		return delaymechanism;
	}
	public void setDelaymechanism(String delaymechanism) {
		this.delaymechanism = delaymechanism;
	}
	public String getAnncintv() {
		return anncintv;
	}
	public void setAnncintv(String anncintv) {
		this.anncintv = anncintv;
	}
	public String getAnnctmo() {
		return annctmo;
	}
	public void setAnnctmo(String annctmo) {
		this.annctmo = annctmo;
	}
	public String getSyncintv() {
		return syncintv;
	}
	public void setSyncintv(String syncintv) {
		this.syncintv = syncintv;
	}
	public String getDelreqintv() {
		return delreqintv;
	}
	public void setDelreqintv(String delreqintv) {
		this.delreqintv = delreqintv;
	}
	public String getPdelreqintv() {
		return pdelreqintv;
	}
	public void setPdelreqintv(String pdelreqintv) {
		this.pdelreqintv = pdelreqintv;
	}
	public String getDelayoffset() {
		return delayoffset;
	}
	public void setDelayoffset(String delayoffset) {
		this.delayoffset = delayoffset;
	}
	public String getTestdelayoffset() {
		return testdelayoffset;
	}
	public void setTestdelayoffset(String testdelayoffset) {
		this.testdelayoffset = testdelayoffset;
	}
	public String getEnable() {
		return enable;
	}
	public void setEnable(String enable) {
		this.enable = enable;
	}
	public String getVlanid() {
		return vlanid;
	}
	public void setVlanid(String vlanid) {
		this.vlanid = vlanid;
	}
	public String getOperationmode() {
		return operationmode;
	}
	public void setOperationmode(String operationmode) {
		this.operationmode = operationmode;
	}
	public String getPortstate() {
		return portstate;
	}
	public void setPortstate(String portstate) {
		this.portstate = portstate;
	}
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public String getTwo_step() {
		return two_step;
	}
	public void setTwo_step(String two_step) {
		this.two_step = two_step;
	}
	
	
}
