package com.nms.drivechenxiao.service.bean.protocols;
/**保护msp
 * **/
public class MspObject {
	private String name;//名称
	private String desc; //描述 ; txt ; 0-16
	private String enaps ; //使能aps ; bool ; ; false
	private String type ; //保护类型 ; 1plus1=0(1+1 aps) ,1to1=1(1:1 aps),1plus1_u=2(1+1_unidirectional Aps) ;1to1
	private String wtrtime ;//等待恢复时间 ; int32,[0,720]
	private String holdofftime ; //拖延时间 ;int32 ,[0,200]
	private String sdthreshold ; //是否使能sd告警触发倒换 ; bool ; ; false
	private String apscmd ; //外部倒换命令 ; txt [0,32];命令格式:clear,lock,froceswith,manualswitch,exercise::(work,protection) ;例如 强制倒换到工作侧(froceswith::protection) ,人工倒换工作侧(manualswitch::protection)
	private String sfhighpri ; //是否sf高优先级 ;bool ; ; false
	private String sdhighpri ; //是否sd高优先级 ;bool ; ; false
	private String prtport ; //保护端口 ; ifname
	private String workport ; //工作端口
	private String apsstat ; //保护状态; lock=0,sf_p=1,fs=2,fs_p=3,sf=4,sd_p=5,sd=6,ms=7,ms_p=8,wtr=9,exer=10,rr=12,dnr=13,nr=14,nr_p=15,clear=16 ;nr=14
	private String sel ; //保护组选择器 ; ifname; [0,16]
	private String clearcnt ; //  ;int32 ;
	private String lastxmitkbyte ;//最近发送的k字节 ; u32; 0x0
	private String lastrecvkbyte ; //最近接收的k字节 ; u32; 0x0
	private String dualid ; // ; int32 ; ; 0
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getEnaps() {
		return enaps;
	}
	public void setEnaps(String enaps) {
		this.enaps = enaps;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getWtrtime() {
		return wtrtime;
	}
	public void setWtrtime(String wtrtime) {
		this.wtrtime = wtrtime;
	}
	public String getHoldofftime() {
		return holdofftime;
	}
	public void setHoldofftime(String holdofftime) {
		this.holdofftime = holdofftime;
	}
	public String getSdthreshold() {
		return sdthreshold;
	}
	public void setSdthreshold(String sdthreshold) {
		this.sdthreshold = sdthreshold;
	}
	public String getApscmd() {
		return apscmd;
	}
	public void setApscmd(String apscmd) {
		this.apscmd = apscmd;
	}
	public String getSfhighpri() {
		return sfhighpri;
	}
	public void setSfhighpri(String sfhighpri) {
		this.sfhighpri = sfhighpri;
	}
	public String getSdhighpri() {
		return sdhighpri;
	}
	public void setSdhighpri(String sdhighpri) {
		this.sdhighpri = sdhighpri;
	}
	public String getPrtport() {
		return prtport;
	}
	public void setPrtport(String prtport) {
		this.prtport = prtport;
	}
	public String getWorkport() {
		return workport;
	}
	public void setWorkport(String workport) {
		this.workport = workport;
	}
	public String getApsstat() {
		return apsstat;
	}
	public void setApsstat(String apsstat) {
		this.apsstat = apsstat;
	}
	public String getSel() {
		return sel;
	}
	public void setSel(String sel) {
		this.sel = sel;
	}
	public String getClearcnt() {
		return clearcnt;
	}
	public void setClearcnt(String clearcnt) {
		this.clearcnt = clearcnt;
	}
	public String getLastxmitkbyte() {
		return lastxmitkbyte;
	}
	public void setLastxmitkbyte(String lastxmitkbyte) {
		this.lastxmitkbyte = lastxmitkbyte;
	}
	public String getLastrecvkbyte() {
		return lastrecvkbyte;
	}
	public void setLastrecvkbyte(String lastrecvkbyte) {
		this.lastrecvkbyte = lastrecvkbyte;
	}
	public String getDualid() {
		return dualid;
	}
	public void setDualid(String dualid) {
		this.dualid = dualid;
	}
	
	
	
}
