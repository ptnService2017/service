package com.nms.drivechenxiao.service.bean.protocols.mpls.llsp;

import java.util.ArrayList;
import java.util.List;

/**端口exp业务映射 里面的 llsp里的 输出
 * **/
public class ClrtoexpObject {
	private String name; //默认0,; int32 ;[1,1] ;
	private String yellow ;//黄色业务到exp映射 ;int32 ; [0,7] ; 0
	private String green; //绿色业务映射到exp映射 ; int32 ; [0,7]; 7
	private List<String> ref = new ArrayList<String>(); //引用clrtoexp接口
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getYellow() {
		return yellow;
	}
	public void setYellow(String yellow) {
		this.yellow = yellow;
	}
	public String getGreen() {
		return green;
	}
	public void setGreen(String green) {
		this.green = green;
	}
	public List<String> getRef() {
		return ref;
	}
	public void setRef(List<String> ref) {
		this.ref = ref;
	}
	
	
}
