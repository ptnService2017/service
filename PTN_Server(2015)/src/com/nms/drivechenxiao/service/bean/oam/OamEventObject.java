package com.nms.drivechenxiao.service.bean.oam;

/**oam事件的上报底层**/
public class OamEventObject {
	private String id;
	private String obj;
	private String iflcl;//是否本地产生事件
	private String timestamp; //产生时间戳
	private String window; //事件周期
	private String threshhold; //门限值
	private String error; //本周期错误信号数
	private String toalerr;//最近一次oam reset 错误信号数
	private String totalevt;//最近一次oam reset 错误信号周期事件数
	private String time; //事件上报时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getObj() {
		return obj;
	}
	public void setObj(String obj) {
		this.obj = obj;
	}
	public String getIflcl() {
		return iflcl;
	}
	public void setIflcl(String iflcl) {
		this.iflcl = iflcl;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getWindow() {
		return window;
	}
	public void setWindow(String window) {
		this.window = window;
	}
	public String getThreshhold() {
		return threshhold;
	}
	public void setThreshhold(String threshhold) {
		this.threshhold = threshhold;
	}
	public String getError() {
		return error;
	}
	public void setError(String error) {
		this.error = error;
	}
	public String getToalerr() {
		return toalerr;
	}
	public void setToalerr(String toalerr) {
		this.toalerr = toalerr;
	}
	public String getTotalevt() {
		return totalevt;
	}
	public void setTotalevt(String totalevt) {
		this.totalevt = totalevt;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}

	
	
	
}
