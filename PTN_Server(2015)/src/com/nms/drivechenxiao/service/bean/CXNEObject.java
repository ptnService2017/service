package com.nms.drivechenxiao.service.bean;

public class CXNEObject {

	private String neIp = "";// 网元IP
	private int nePort = 3333;// 网元管理端口
	private String admin = "";// 用户名
	private String password = "";// 密码

	public int getNePort() {
		return nePort;
	}

	public void setNePort(int nePort) {
		this.nePort = nePort;
	}

	public String getAdmin() {
		return admin;
	}

	public void setAdmin(String admin) {
		this.admin = admin;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getNeIp() {
		return neIp;
	}

	public void setNeIp(String neIp) {
		this.neIp = neIp;
	}
}
