package com.nms.ui.ptn.alarm.model;

import java.io.Serializable;

public class AlarmLineInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -1426794497981626840L;
	private int lineNumber;//线路号或告警代码
	private String lineName;//线路名称
	private String type;//用于标记是否是同类型的
	private String codeName;//告警代码简称
	
	public String getLineName() {
		return lineName;
	}
	public void setLineName(String lineName) {
		this.lineName = lineName;
	}
	public int getLineNumber() {
		return lineNumber;
	}
	public void setLineNumber(int lineNumber) {
		this.lineNumber = lineNumber;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}
	
	
}
